// ─── Desktop horizontal scroll ────────────────────────────────────────────────
function HorizontalScroll({ isDark }) {
  const sectionRef = useRef(null)
  const [activeIdx, setActiveIdx] = useState(0)

  const { scrollYProgress } = useScroll({
    target:  sectionRef,
    offset:  ['start start', 'end end'],
  })

  // x translation: card 0 → card 5 as scrollYProgress 0 → 1
  const x = useTransform(
    scrollYProgress,
    [0, 1],
    [0, -(PROBLEMS.length - 1) * CARD_STRIDE],
  )

  useMotionValueEvent(scrollYProgress, 'change', (v) => {
    setActiveIdx(Math.min(Math.round(v * (PROBLEMS.length - 1)), PROBLEMS.length - 1))
  })

  const sectionH = `${(PROBLEMS.length - 1) * 100 + 220}vh`

  // live background glow color for active card
  const activeGlow = isDark
    ? PROBLEMS[activeIdx].glowD.replace('0.2)', '0.12)')
    : PROBLEMS[activeIdx].glowL.replace('0.1)', '0.06)')

  return (
    <div ref={sectionRef} style={{ height: sectionH }} className="relative">
      {/* Sticky viewport */}
      <div className="sticky top-0 h-screen overflow-hidden flex flex-col" aria-label="Scroll through problems">

        {/* Dynamic background orb that shifts color with active card */}
        <motion.div
          aria-hidden="true"
          className="absolute rounded-full pointer-events-none"
          animate={{ background: `radial-gradient(circle,${activeGlow} 0%,transparent 70%)` }}
          transition={{ duration: 0.7 }}
          style={{ width: 700, height: 700, top: '15%', left: '50%', translateX: '-50%', zIndex: 0 }}
        />

        <div className="relative flex flex-col flex-1 justify-center" style={{ zIndex: 1 }}>
          {/* Header */}
          <SectionHeader isDark={isDark} />

          {/* Card track */}
          <div className="overflow-hidden" aria-live="polite" aria-atomic="true">
            <motion.div
              style={{
                display:    'flex',
                gap:        CARD_GAP,
                x,
                marginLeft: `calc(50vw - ${CARD_W / 2}px)`,
                willChange: 'transform',
              }}
            >
              {PROBLEMS.map((p, i) => (
                <HorizontalCard
                  key={p.title}
                  problem={p}
                  isActive={i === activeIdx}
                  isDark={isDark}
                />
              ))}
            </motion.div>
          </div>

          {/* Progress */}
          <ProgressBar activeIdx={activeIdx} isDark={isDark} />
        </div>
      </div>
    </div>
  )
}

// ─── ProblemSection ───────────────────────────────────────────────────────────

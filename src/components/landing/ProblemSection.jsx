      {/* Static background (noise + grid — always shown) */}
      <BackgroundGlow isDark={isDark} accentColor={isDark ? 'rgba(239,68,68,0.07)' : 'rgba(239,68,68,0.04)'} />

      {/* Desktop: horizontal scroll section */}

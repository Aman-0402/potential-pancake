// ─── Navigation ───────────────────────────────────────────────────────────────
function Navigation({ activeIdx, isDark, onDot, onPrev, onNext, paused }) {
  const p = PROBLEMS[activeIdx]

  return (
    <div className="flex flex-col items-center gap-3 mt-10">
      {/* Auto-play timer bar — resets on each card */}
      <div className="w-48 h-0.5 rounded-full overflow-hidden" style={{ background: cv(700, 0.2) }}>
        <motion.div
          key={activeIdx}                       // restart animation on card change
          className="h-full rounded-full"
          style={{ background: p.accent }}
          initial={{ width: '0%' }}
          animate={{ width: paused ? undefined : '100%' }}
          transition={{ duration: AUTO_MS / 1000, ease: 'linear' }}
        />
      </div>

      {/* Dots + arrows */}
      <div className="flex items-center gap-4">
        {/* Prev */}
        <motion.button
          onClick={onPrev}
          disabled={activeIdx === 0}
          whileHover={{ scale: 1.1 }} whileTap={{ scale: 0.92 }}
          aria-label="Previous problem"
          className="w-9 h-9 rounded-full flex items-center justify-center transition-colors"
          style={{
            background: cv(800, 0.5),
            border: `1px solid ${cv(700, 0.2)}`,
            opacity: activeIdx === 0 ? 0.3 : 1,
          }}
        >
          <ChevronLeft className="w-4 h-4 text-dark-300" />
        </motion.button>

        {/* Dots */}
        <div className="flex items-center gap-2" role="tablist" aria-label="Problem navigation">
          {PROBLEMS.map((pr, i) => (
            <motion.button
              key={i}
              role="tab"
              aria-selected={i === activeIdx}
              aria-label={`Problem ${i+1}: ${pr.title}`}
              onClick={() => onDot(i)}
              animate={{
                width:   i === activeIdx ? 28 : 8,
                background: i === activeIdx ? pr.accent : cv(700, 0.35),
                opacity: i === activeIdx ? 1 : 0.55,
              }}
              transition={{ duration: 0.3, ease: EASE }}
              className="h-2 rounded-full cursor-pointer"
            />
          ))}
        </div>

        {/* Next */}
        <motion.button
          onClick={onNext}
          disabled={activeIdx === PROBLEMS.length - 1}
          whileHover={{ scale: 1.1 }} whileTap={{ scale: 0.92 }}
          aria-label="Next problem"
          className="w-9 h-9 rounded-full flex items-center justify-center"
          style={{
            background: cv(800, 0.5),
            border: `1px solid ${cv(700, 0.2)}`,
            opacity: activeIdx === PROBLEMS.length - 1 ? 0.3 : 1,
          }}
        >
          <ChevronRight className="w-4 h-4 text-dark-300" />
        </motion.button>
      </div>
    </div>
  )
}

// ─── Horizontal carousel (desktop) ────────────────────────────────────────────
function HorizontalCarousel({ isDark }) {
  const [activeIdx, setActiveIdx] = useState(0)
  const [paused,    setPaused]    = useState(false)
  const reduced = useReducedMotion()

  // Smooth spring-driven x — no scroll-jacking, no tall container
  const xTarget = useMotionValue(0)
  const x       = useSpring(xTarget, { stiffness: 200, damping: 30, mass: 0.8 })

  useEffect(() => {
    xTarget.set(-activeIdx * CARD_STRIDE)
  }, [activeIdx, xTarget])

  // Auto-advance
  useEffect(() => {
    if (paused || reduced) return
    const t = setInterval(() => {
      setActiveIdx(i => (i + 1) % PROBLEMS.length)
    }, AUTO_MS)
    return () => clearInterval(t)
  }, [paused, reduced])

  // Keyboard nav (left/right arrows)
  useEffect(() => {
    const onKey = (e) => {
      if (e.key === 'ArrowRight') { setActiveIdx(i => Math.min(i+1, PROBLEMS.length-1)); setPaused(true) }
      if (e.key === 'ArrowLeft')  { setActiveIdx(i => Math.max(i-1, 0));                 setPaused(true) }
    }
    window.addEventListener('keydown', onKey)
    return () => window.removeEventListener('keydown', onKey)
  }, [])

  const goTo = (i) => { setActiveIdx(i); setPaused(true) }
  const prev = ()  => { setActiveIdx(i => Math.max(i-1, 0));                 setPaused(true) }
  const next = ()  => { setActiveIdx(i => Math.min(i+1, PROBLEMS.length-1)); setPaused(true) }

  const activeGlow = isDark
    ? PROBLEMS[activeIdx].glowD.replace('0.18)', '0.12)')
    : PROBLEMS[activeIdx].glowL.replace('0.09)', '0.05)')

  return (
    <motion.div
      onMouseEnter={() => setPaused(true)}
      onMouseLeave={() => setPaused(false)}
      initial={{ opacity: 0, y: 24, filter: 'blur(8px)' }}
      whileInView={{ opacity: 1, y: 0, filter: 'blur(0px)' }}
      viewport={{ once: true, margin: '-60px' }}
      transition={{ duration: 0.6, ease: EASE }}
    >
      {/* Dynamic accent orb */}
      <motion.div
        aria-hidden="true"
        className="absolute left-1/2 pointer-events-none"
        style={{ translateX: '-50%', width: 600, height: 400, top: '40%', zIndex: 0 }}
        animate={{ background: `radial-gradient(ellipse 60% 60% at 50% 50%,${activeGlow} 0%,transparent 70%)` }}
        transition={{ duration: 0.7 }}
      />

      {/* Card track */}
      <div className="overflow-hidden" style={{ position: 'relative', zIndex: 1 }}>
        <motion.div
          style={{
            display:    'flex',
            gap:        CARD_GAP,
            x,
            marginLeft: `calc(50vw - ${CARD_W / 2}px)`,
            willChange: 'transform',
          }}
        >
          {PROBLEMS.map((p, i) => (
            <HorizontalCard key={p.title} problem={p} isActive={i === activeIdx} isDark={isDark} />
          ))}
        </motion.div>
      </div>

      <Navigation
        activeIdx={activeIdx}
        isDark={isDark}
        onDot={goTo}
        onPrev={prev}
        onNext={next}
        paused={paused}
      />
    </motion.div>
  )
}

// ─── Vertical fallback (mobile + reduced-motion) ───────────────────────────────
